<h1>Business Logics</h1>

<h3>1. Many sites, one project</h3>
<p>1.1. One site is manageable by 2 site manager.</p>
<p>1.2. Sites are grouped in a cluster. And a cluster has only 1 clusterlead.</p>
<h3>2. Approvalism</h3>
<p>Number of action upon contents of certain submodules created for a site (by <strong>sitemanager</strong>) will require an approval by the cluster lead. <br>
Such as :
</p>
<ul>
	<li>Site : update</li>
	<li>Announcement : add, update</li>
	<li>Article : add, update</li>
	<li>Activity : add, update</li>
	<li>Video : add, update</li>
	<li>Forum Category : add, update</li>
</ul>
