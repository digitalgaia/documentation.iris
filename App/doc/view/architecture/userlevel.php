<p>Not sure what he does.</p>
<h3>2.4. Financial Controller</h3>
<p>Must probably be something about requisition or something like that.</p>
<h3>2.5. Root</h3>
<p>The super level of pi1m. This account have access to major data like every sites information, although not necessarily know every content created by a site. This level may even create a new site.</p>