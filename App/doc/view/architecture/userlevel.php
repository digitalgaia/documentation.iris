<h1>User Level (role)</h1>
<p>Available level of access in IRIS, defined in model@user/user.</p>
<ul>
	<li>1. Site member</li>
	<li>2. Site Manager</li>
	<li>3. Cluster lead</li>
	<li>4. Operation Manager</li>
	<li>5. Financial Controller</li>
	<li>99. Root</li>
	<li>999. Developer</li>
</ul>
<h2>1. Accessibility</h2>
<p>Every level of user may access <strong>backend</strong> module except, <strong>member</strong>. <br>And, <strong>frontend</strong> module is accessible to any level user.</p>
<h2>2. User Role</h2>
<h3>2.1. Site member</h3>
<p>The main level of user that constitute 99.9% of user in Pi1M. Each member may only be linked to one site. It takes registration fee of RM 5 (if am not mistaken) to activate the user, and only site manager may approve/activate the member.</p>
<h3>2.2. Site manager</h3>
<p>The main level to user to manage a site. Each site manager may only manage one site, and a site may have not more than 2 manager. A manager have every access to edit, update every content and delete certain off the site, and some of it would require an approval from clusterlead.</p>
